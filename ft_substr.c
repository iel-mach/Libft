#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*res;
	size_t	i;
	size_t	j;

	if (!s)
		return (0);
	if (start >= ft_strlen(s))
		len = 0;
	i = 0;
	j = 0;
	while (i < len && s[i])
		i++;
	if (!(res = malloc(i + 1)))
		return (NULL);
	while (j < i)
		res[j++] = s[start++];
	res[i] = '\0';
	return (res);
}